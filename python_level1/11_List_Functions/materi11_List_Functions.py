contoh = [3, 7, 1, 5, 51, 73, 15, 37]
# fungsi len() (Length) : menghitung jumlah item di list
print("Contoh Len():", len(contoh))

# fungsi sum() (Summation) : menjumlahkan angka di dalam list
print("Contoh sum():", sum(contoh))

# fungsi max() dan min() : untuk mencari nilai tertinggi dan terendah di dalam list
print("Contoh max():", max(contoh))
print("Contoh min():", min(contoh))