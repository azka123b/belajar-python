angka = [3, 7, 1, 5, 51, 73, 15, 37]

print("Hasil Len():", len(angka))
print("Hasil sum():", sum(angka))
print("Hasil max():", max(angka))
print("Hasil min():", min(angka))

rata2 = sum(angka) / len(angka)

print("Rata-Rata dari list ini (setelah dibulatkan) adalah:", round(rata2))
